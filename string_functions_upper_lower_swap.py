# Input
a = input("Enter your string: ")
print (f"You entered :{a}")

# Lower case
b = str.lower(a)
print(f"The lower case of the string you wrote is: {b}")

# Upper case
c = str.upper(b)
print(f"The upper case of the string you wrote is: {c}")

# Swap case
d = str.swapcase(c)
print(f"The swapped case of the previous string you wrote is: {d}")